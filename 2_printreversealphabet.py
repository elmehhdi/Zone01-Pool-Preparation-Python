# nloopiw through characters men 'z' to 'a'
# range(start,stop,step) fonctionnement dyal range
# ord() fonction katconverté men charactere l ascii code
for c in range(ord('z'), ord('a') - 1, -1):  # Step hna ndiro -1 bach nweliw b lor
    print(chr(c), end='')  # chr() nconvertéw biha men ascii l charactere et end='' bach manprintiwch ster jdid f kol iteration

print()  # Print a newline after the loop
